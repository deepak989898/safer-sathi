interface VehicleSeedConfig {
  id: string;
  slug: string;
  name: string;
  brand: string;
  category: string;
  type: VehicleType;
  seats: number;
  fuelType: string;
  pricePerDay: number;
  pricePerKm: number;
  rating: number;
  reviewCount: number;
  features: string[];
  description: string;
}

function buildVehicle(cfg: VehicleSeedConfig): Vehicle {
  const now = new Date().toISOString();
  return {
    id: cfg.id,
    slug: cfg.slug,
    name: enHi(cfg.name),
    brand: cfg.brand,
    category: cfg.category,
    type: cfg.type,
    seats: cfg.seats,
    fuelType: cfg.fuelType,
    pricePerDay: cfg.pricePerDay,
    pricePerKm: cfg.pricePerKm,
    images: getVehicleImageUrls(cfg.slug),
    available: true,
    status: "active",
    driverIncluded: true,
    description: enHi(cfg.description),
    features: cfg.features,
    rating: cfg.rating,
    reviewCount: cfg.reviewCount,
    location: "Delhi NCR · Pan India",
    createdAt: now,
    updatedAt: now,
  };
}

