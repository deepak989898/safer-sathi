import { packageImagePaths } from "@/lib/media/package-images";
import type {
  LocalizedString,
  PackageCategory,
  PackageItineraryDay,
  TourPackage,
} from "@/types";

function enHi(en: string, hi?: string): LocalizedString {
  return { en, hi: hi ?? en };
}

function inc(...items: string[]): LocalizedString[] {
  return items.map((i) => enHi(i));
}

function buildItinerary(
  days: { title: string; description: string; activities?: string[] }[]
): PackageItineraryDay[] {
  return days.map((d, i) => ({
    day: i + 1,
    title: enHi(d.title),
    description: enHi(d.description),
    activities: d.activities ?? [],
  }));
}

interface SeedConfig {
  id: string;
  slug: string;
  title: LocalizedString;
  cities: string[];
  nights: number;
  days: number;
  category: PackageCategory;
  price: number;
  originalPrice: number;
  rating: number;
  reviewCount: number;
  featured: boolean;
  overview: LocalizedString;
  activities: string[];
  hotels: string[];
  inclusions: string[];
  exclusions: string[];
  transport: LocalizedString;
  meals: string[];
  itinerary: { title: string; description: string; activities?: string[] }[];
}

function buildPackage(cfg: SeedConfig): TourPackage {
  const now = new Date().toISOString();
  return {
    id: cfg.id,
    slug: cfg.slug,
    title: cfg.title,
    category: cfg.category,
    duration: cfg.days,
    durationLabel: enHi(
      `${cfg.nights} Nights / ${cfg.days} Days`,
      `${cfg.nights} रातें / ${cfg.days} दिन`
    ),
    cities: cfg.cities,
    hotels: cfg.hotels,
    meals: cfg.meals,
    activities: cfg.activities,
    price: cfg.price,
    originalPrice: cfg.originalPrice,
    images: packageImagePaths(cfg.slug),
    description: cfg.overview,
    itinerary: buildItinerary(cfg.itinerary),
    inclusions: inc(...cfg.inclusions),
    exclusions: inc(...cfg.exclusions),
    transport: cfg.transport,
    rating: cfg.rating,
    reviewCount: cfg.reviewCount,
    featured: cfg.featured,
    publishStatus: "published",
    proposedBy: "admin",
    createdAt: now,
    updatedAt: now,
  };
}

